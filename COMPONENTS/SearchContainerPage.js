function calculate_time(time){
  let min = time/60000
  let sec = Math.floor(Math.random() * 59);
  min = Math.floor(min)
  return (min +":"+sec)
}

function song_name(data){
  let name =  data.split(" ")
  
  let sname =''
  for(var i = 0; i<name.length && i<4; i++){
    sname += name[i]+" "
  }
  if(name.length>3){
    return sname +"..."
  }
  
  return sname;
}



function get_singer_name(data){
  let sin = ""
  for(var i = 0; i<data.length; i++){
    sin += data[i].name+" ";
  }
  return sin;
}



